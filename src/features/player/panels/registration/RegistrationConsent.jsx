export default function RegistrationConsent({ policyText, onAccept, onClose }) {
  const [checkedPolicy, setCheckedPolicy] = useState(false)
  const [checkedAge, setCheckedAge]       = useState(false)
  const text = policyText || DEFAULT_POLICY
  const canAccept = checkedPolicy && checkedAge

  return (
    <div className="regConsentOverlay" onClick={onClose}>
      <div className="regConsentModal" onClick={e => e.stopPropagation()}>
        <div className="regConsentHeader">
          <span className="regConsentTitle">Политика конфиденциальности</span>
          <button className="regConsentClose" onClick={onClose}>✕</button>
        </div>
        <div className="regConsentBody">
          <pre className="regConsentText">{text}</pre>
        </div>
        <div className="regConsentFooter">
          <label className="regConsentCheck">
            <input
              type="checkbox"
              checked={checkedPolicy}
              onChange={e => setCheckedPolicy(e.target.checked)}
            />
            <span>Я ознакомился(-ась) и принимаю условия политики обработки персональных данных</span>
          </label>
          <label className="regConsentCheck">
            <input
              type="checkbox"
              checked={checkedAge}
              onChange={e => setCheckedAge(e.target.checked)}
            />
            <span>Мне исполнилось 18 лет</span>
          </label>
          <button
            className="regConsentAccept"
            disabled={!canAccept}
            onClick={onAccept}
          >
            Принять
          </button>
        </div>
      </div>
    </div>
  )
}
