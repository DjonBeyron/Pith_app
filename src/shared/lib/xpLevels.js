// Пороги 0-10 — исторические (не менять, см. выше). С 11-го — плавно растущий
// шаг (было бы странно, если после уровня «на максимуме» шаг снова упал до 100):
// начинаем с +700 и на каждом следующем уровне добавляем ещё +6 к шагу.
// К 100-му уровню это даёт ~95 000 XP — иначе не рукописный подбор, а
// прозрачная формула, которую видно и можно поправить одним числом
function buildXpTable() {
  const early = [0, 100, 250, 500, 1000, 1600, 2400, 3400, 4600, 6000, 8000]
  const table = [...early]
  let delta = 700
  for (let n = 11; n <= 100; n++) {
    table.push(table[n - 1] + delta)
    delta += 6
  }
  return table
}

const XP_TABLE = buildXpTable()

export const LEVELS = LABELS.map((label, level) => ({ level, label, xpNeeded: XP_TABLE[level] }))

export function getCurrentLevel(xp) {
  let current = LEVELS[0]
  for (const lvl of LEVELS) {
    if (xp >= lvl.xpNeeded) current = lvl
  }
  return current
}

export function getNextLevel(xp) {
  for (const lvl of LEVELS) {
    if (xp < lvl.xpNeeded) return lvl
  }
  return null
}
