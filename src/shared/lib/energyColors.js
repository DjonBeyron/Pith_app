// Цвет энергии по текущему количеству (не по проценту): чем меньше энергии,
// тем тревожнее цвет, на максимуме — фирменный лайм. Используется везде, где
// показывается число/индикатор энергии (EnergyCells, EnergyBadge, профиль...).
const COLORS = [
  '#f87171', // 0 — красный, энергии нет
  '#f87171', // 1 — красный, тревожно мало
  '#facc15', // 2 — жёлтый
  '#d3e34f', // 3 — жёлто-зелёный
  '#9fe6c0', // 4 — зелёно-лавандовый (мятный с сиреневым подтоном)
  '#b6fe3b', // 5 — фирменный лайм, полная энергия
]

// Лавандовая рамка-акцент для значения 4 — подчёркивает сиреневый подтон
export const ENERGY_4_BORDER = '#c9b8f5'

export function energyColor(value) {
  const v = Math.max(0, Math.min(5, Math.round(value ?? 0)))
  return COLORS[v]
}
